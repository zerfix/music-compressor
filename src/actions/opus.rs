use std::io;
use std::path::PathBuf;
use std::process::Command;

//-////////////////////////////////////////////////////////////////////////////
//  Opus
//-////////////////////////////////////////////////////////////////////////////
/// Compress lossless to opus with shell command.
pub fn compress(input_path: &PathBuf, output_path: &PathBuf) -> Result<(), io::Error> {
    Command::new("opusenc")
        .arg("--downmix-stereo")
        .arg(input_path)
        .arg(output_path)
        .output()?;
    Ok(())
}
