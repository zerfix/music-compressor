// std
use std::io;
use std::process::Command;

//-////////////////////////////////////////////////////////////////////////////
// Opus
//-////////////////////////////////////////////////////////////////////////////
pub fn compress(input_path: String, output_path: String) -> Result<(), io::Error> {
    Command::new("opusenc")
        .arg("--downmix-stereo")
        .arg(input_path)
        .arg(output_path)
        .output()?;

    Ok(())
}
